public class loopsbasics {
    public static void main(String[] args) {
        //print hello world 100 times
        int counter = 0;
        while (counter < 100) {
            System.out.println("hello world");
            counter++;
        }
        System.out.println("printed hello world 100 times");

        //infinite time cse in while loop ; when condition is = "true" in this casse output is unreachable it will tend to infinite times
        // ye code tabhi rukenga jab apki memory bhar jayengi



        //print number from 1 to 10
        int number = 1; 
        while (number <= 1000) {
            //System.out.println(number);
            System.out.print(number+" ");
            number++;
            
        }
    }
    
}

